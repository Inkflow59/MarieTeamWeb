    <script>
        const slider = document.querySelector('.actualites-slider');
        const prevButton = document.querySelector('.prev');
        const nextButton = document.querySelector('.next');
        let isDragging = false;
        let startX;
        let scrollLeft;

        // Gestion du défilement avec la souris
        slider.addEventListener('mousedown', (e) => {
            isDragging = true;
            slider.classList.add('grabbing');
            startX = e.pageX - slider.offsetLeft;
            scrollLeft = slider.scrollLeft;
        });

        slider.addEventListener('mouseleave', () => {
            isDragging = false;
            slider.classList.remove('grabbing');
        });

        slider.addEventListener('mouseup', () => {
            isDragging = false;
            slider.classList.remove('grabbing');
        });

        slider.addEventListener('mousemove', (e) => {
            if (!isDragging) return;
            e.preventDefault();
            const x = e.pageX - slider.offsetLeft;
            const walk = (x - startX) * 2;
            slider.scrollLeft = scrollLeft - walk;
        });

        // Gestion des boutons de navigation
        nextButton.addEventListener('click', () => {
            slider.scrollBy({
                left: 300,
                behavior: 'smooth'
            });
            updateProgressBar();
        });

        prevButton.addEventListener('click', () => {
            slider.scrollBy({
                left: -300,
                behavior: 'smooth'
            });
            updateProgressBar();
        });

        // Mise à jour de la barre de progression
        function updateProgressBar() {
            const progress = (slider.scrollLeft / (slider.scrollWidth - slider.clientWidth)) * 100;
            document.querySelector('.progress-bar').style.width = `${progress}%`;
        }

        // Mise à jour de la barre de progression lors du défilement
        slider.addEventListener('scroll', () => {
            updateProgressBar();
        });

        // Initialisation de la barre de progression
        updateProgressBar();
    </script>
